def generate_points(n):
    points = []
    k = 100 # k - радиус окружности
    d_alfa = 2 * math.pi / n
    for i in range(n):
        x = k + k * math.sin(i * d_alfa)
        y = k + k * math.cos(i * d_alfa)
        points.append([x, y])
    return points
